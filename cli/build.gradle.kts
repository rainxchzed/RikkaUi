// ─── Generate CliVersion.kt from gradle.properties ─────────
val generateVersion by tasks.registering {
    val versionName = project.property("VERSION_NAME") as String
    val groupId = project.property("GROUP") as String
    val outputDir = layout.buildDirectory.dir("generated/version/kotlin")

    inputs.property("versionName", versionName)
    outputs.dir(outputDir)

    doLast {
        val dir = outputDir.get().asFile.resolve(
            "zed/rainxch/rikkaui/cli",
        )
        dir.mkdirs()
        dir.resolve("CliVersion.kt").writeText(
            """
            |// Generated by Gradle — do not edit.
            |package zed.rainxch.rikkaui.cli
            |
            |object CliVersion {
            |    const val NAME = "$versionName"
            |    const val FOUNDATION = "$groupId:foundation:$versionName"
            |    const val COMPONENTS = "$groupId:components:$versionName"
            |}
            """.trimMargin(),
        )
    }
}

kotlin.sourceSets.main {
    kotlin.srcDir(generateVersion.map { it.outputs.files.singleFile })
}

tasks.shadowJar {
    archiveBaseName.set("rikkaui")
    archiveClassifier.set("")
    archiveVersion.set("")
    manifest {
        attributes("Main-Class" to "zed.rainxch.rikkaui.cli.MainKt")
    }
}
